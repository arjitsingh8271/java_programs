
class operators_1_arithmetic
{
	public static void main(String args[])
	{
		int a = 11;
		int b = 3;

		System.out.println("a + b: " +(a+b));
		System.out.println("a - b: " +(a-b));
		System.out.println("a * b: " +(a*b));
		System.out.println("a / b: " +(a/b));
		System.out.println("a % b: " +(a%b));
	}
}



/*
OUTPUT:	a + b: 14
		a - b: 8
		a * b: 33
		a / b: 3
		a % b: 2
*/